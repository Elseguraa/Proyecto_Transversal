    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        
        Materia m = new Materia();

    String nombre = jTNombre.getText();
    String anioTexto = jTAnio.getText();


if (nombre.isEmpty() || anioTexto.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Error: Todos los campos deben ser completados");
    return; 
}

try {
    m.setNombre(nombre);
    m.setAnio(Integer.parseInt(anioTexto));
    md.agregarMateria(m);
    clear();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Error: Debe ingresar un número válido en el campo de año");
}
                  
       

        
    }//GEN-LAST:event_jBGuardarActionPerformed

