    private void mostrarMateria(ArrayList<Materia> materias){
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        for (Materia materia : materias) {
            Object[] fila = {materia.getId_materia(), materia.getNombre(), materia.getAnio(), materia.isEstado()};
            tableModel.addRow(fila);
        }
    }
    
